  UIManager::UIManager(
      HWND hwnd,
      ID3D11Device *device,
      ID3D11DeviceContext *deviceContext,
      float width,
      float height
      )
  {
    ImGui::CreateContext();
    auto *io  = &ImGui::GetIO();
    io->ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;
    io->DisplaySize = ImVec2(width, height);
    io->IniFilename = nullptr;
    ApplyStyle();
    LoadFont();

    ImGui_ImplWin32_Init(hwnd);
    ImGui_ImplDX11_Init(device, deviceContext);
  }

  UIManager::~UIManager()
  {
    ImGui_ImplDX11_Shutdown();
    ImGui_ImplWin32_Shutdown();
    ImGui::DestroyContext();
  }

  void UIManager::SetDisplaySize(float width, float height)
  {
    ImGui::GetIO().DisplaySize = ImVec2(width, height);
  }

  void UIManager::SetCanvas(Modules::Canvas &p_canvas)
  {
    RemoveCanvas();
    m_currentCanvas = &p_canvas;
  }

  void UIManager::RemoveCanvas()
  {
    m_currentCanvas = nullptr;
  }

  void UIManager::Render()
  {
    if(PRESENT_PTR(m_currentCanvas))
    {
      m_currentCanvas->Draw();
      ImGui_ImplDX11_RenderDrawData(ImGui::GetDrawData());
    }
  }

